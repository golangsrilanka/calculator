package internal

func AddTwoNumbers(num1 uint64, num2 uint64) uint64 {
	return num1 + num2
}

func SubtractTwoNumbers(num1 uint64, num2 uint64) uint64 {
	return num1 - num2
}

func DivideTwoNumbers(num1 uint64, num2 uint64) uint64 {
	return num1 / num2
}

func MultiplyTwoNumbers(num1 uint64, num2 uint64) uint64 {
	return num1 * num2
}
